    private void popuniInicijalno() {
        txtEmail.setEnabled(false);
        txtImePrezime.setEnabled(false);
        txtTelefon.setEnabled(false);
        txtKategorija.setEnabled(false);
        txtLozinka.setEnabled(false);
        btnIzmeniPodatke.setVisible(true);
        btnOdustani.setVisible(false);
        btnPotvrdiIzmenu.setVisible(false);

        txtEmail.setText(klijent.getEmail());
        txtImePrezime.setText(klijent.toString());
        txtTelefon.setText(klijent.getTelefon());
        txtKategorija.setText(klijent.getKategorija().toString());
        txtLozinka.setText(klijent.getLozinka());
        popuniCbUgovori();
    }

    private void popuniCbUgovori() {
        cbUgovori.removeAllItems();

        List<Ugovor> ugovori = communication.vratiSveUgovore();
        List<Ugovor> filterUgovori = new ArrayList<>();

        for (Ugovor u : ugovori) {
            if (u.getKlijent().equals(klijent)) {
                filterUgovori.add(u);
            }
        }

        for (Ugovor u : filterUgovori) {
            cbUgovori.addItem(u);
        }
    }


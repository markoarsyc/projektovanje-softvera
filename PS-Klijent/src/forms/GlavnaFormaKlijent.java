    private void btnPotvrdiIzmenuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPotvrdiIzmenuActionPerformed
        String noviEmail = txtEmail.getText();
        String novaLozinka = String.valueOf(txtLozinka.getPassword());
        String noviTelefon = txtTelefon.getText();

        Klijent izmenjeniKlijent = new Klijent();
        izmenjeniKlijent.setIdKlijent(klijent.getIdKlijent());
        izmenjeniKlijent.setEmail(noviEmail);
        izmenjeniKlijent.setTelefon(noviTelefon);
        izmenjeniKlijent.setLozinka(novaLozinka);

        int result = communication.izmeniKlijenta(klijent);
        if (result != 0) {
            JOptionPane.showMessageDialog(this, "Uspesna izmena podataka", "Uspesno", JOptionPane.INFORMATION_MESSAGE);
            klijent.setEmail(noviEmail);
            klijent.setLozinka(novaLozinka);
            klijent.setTelefon(noviTelefon);
            popuniInicijalno();
        } else {
            JOptionPane.showMessageDialog(this, "Greska prilikom izmene podataka", "Greska", JOptionPane.ERROR_MESSAGE);
            popuniInicijalno();
        }
    }//GEN-LAST:event_btnPotvrdiIzmenuActionPerformed


    public void popuniTabeluRadnoVreme() {
        List<RadnoVreme> radnaVremena = communication.vratiSveRadnoVreme();
        List<RadnoVreme> radnaVremenaZaposleni = new ArrayList<>();
        for (RadnoVreme rv : radnaVremena) {
            if (rv.getZaposleni().equals(zaposleni)) {
                radnaVremenaZaposleni.add(rv);
            }
        }
        TableModelRadnoVreme tmrv = new TableModelRadnoVreme(radnaVremenaZaposleni);
        tblRadnoVreme.setModel(tmrv);
    }


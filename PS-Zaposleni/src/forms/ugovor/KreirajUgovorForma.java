    private void cbTipPaketaUslugaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbTipPaketaUslugaActionPerformed
        List<PaketUsluga> paketi = communication.vratiSvePakete();
        List<PaketUsluga> filterPaketi = new ArrayList<>();
        TipPaketaUsluga selektovaniTip = (TipPaketaUsluga) cbTipPaketaUsluga.getSelectedItem();
        for (PaketUsluga paket : paketi) {
            if (paket.getTip().equals(selektovaniTip)) {
                filterPaketi.add(paket);
            }
        }
        cbPaketiUsluga.removeAllItems();
        for (PaketUsluga paket : filterPaketi) {
            cbPaketiUsluga.addItem(paket);
        }
        cbPaketiUsluga.setEnabled(true);
    }//GEN-LAST:event_cbTipPaketaUslugaActionPerformed

    private void btnDodajStavkuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajStavkuActionPerformed
        int procenatPopusta = klijent.getKategorija().getProcenatPopusta();
        PaketUsluga odabraniPaket = (PaketUsluga) cbPaketiUsluga.getSelectedItem();
        if (odabraniPaket == null) {
            JOptionPane.showMessageDialog(this, "Morate prvo odabrati paket", "Gre≈°ka", JOptionPane.ERROR_MESSAGE);
            return;
        }
        double osnovnaCenaPaketa = odabraniPaket.getCenaMesec();
        double cenaSaPopustom = osnovnaCenaPaketa - (osnovnaCenaPaketa * procenatPopusta / 100);
        StavkaUgovora novaStavkaUgovora = new StavkaUgovora();
        novaStavkaUgovora.setPaketUsluga(odabraniPaket);
        novaStavkaUgovora.setFinalnaCena(cenaSaPopustom);
        if (stavkeUgovora.contains(novaStavkaUgovora)) {
            JOptionPane.showMessageDialog(this, "Ovaj paket veƒá postoji", "Gre≈°ka", JOptionPane.ERROR_MESSAGE);
            return;
        }
        stavkeUgovora.add(novaStavkaUgovora);
        popuniTabeluStavkeUgovora();
        izracunajCenuUgovora();
    }//GEN-LAST:event_btnDodajStavkuActionPerformed

